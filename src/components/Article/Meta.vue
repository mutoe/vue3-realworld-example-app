<template>
  <div class="article-meta">
    <AppLink
      name="profile"
      :params="{username: article.author.username}"
    >
      <img :src="article.author.image">
    </AppLink>

    <div class="info">
      <AppLink
        name="profile"
        :params="{username: article.author.username}"
        class="author"
      >
        {{ article.author.username }}
      </AppLink>

      <span class="date">{{ (new Date(article.createdAt)).toLocaleDateString() }}</span>
    </div>

    <button
      v-if="displayFollow"
      class="btn btn-sm btn-outline-secondary space"
      :disabled="followProcessGoing"
      @click="toggleFollow"
    >
      <i class="ion-plus-round space" />
      {{ article.author.following ? "Unfollow" : "Follow" }} {{ article.author.username }}
    </button>

    <button
      class="btn btn-sm space"
      :class="[article.favorited ? 'btn-primary':'btn-outline-primary']"
      :disabled="favoriteProcessGoing"
      @click="favoriteArticle"
    >
      <i class="ion-heart space" />
      {{ article.favorited ? 'Unfavorite' : 'Favorite' }} Article
      <span class="counter">({{ article.favoritesCount }})</span>
    </button>

    <AppLink
      v-if="displayEditButton"
      class="btn btn-outline-secondary btn-sm space"
      name="edit-article"
      :params="{slug: article.slug}"
    >
      <i class="ion-edit space" /> Edit Article
    </AppLink>

    <button
      v-if="displayEditButton"
      class="btn btn-outline-danger btn-sm"
      @click="onDelete"
    >
      <i class="ion-trash-a" /> Delete Article
    </button>
  </div>
</template>

<script lang="ts">
import { computed, defineComponent, PropType, toRefs } from 'vue'
import { useRouter } from 'vue-router'
import { useStore } from 'vuex'

import { deleteArticle } from '../../services/article/deleteArticle'
import { useFavoriteArticle } from '../../services/article/favoriteArticle'
import { useFollow } from '../../services/profile/followProfile'

import { Store } from '../../store'

export default defineComponent({
  name: 'ArticleMeta',
  props: {
    article: { type: Object as PropType<Article>, required: true },
  },
  emits: {
    update: (article: Article) => !!article.slug,
  },
  setup (props, { emit }) {
    const router = useRouter()
    const store = useStore<Store>()
    const user = computed<Store['user']>(() => store.state.user)
    const { article } = toRefs(props)

    const displayEditButton = computed(() => user.value?.username === article.value.author.username)

    const { favoriteProcessGoing, favoriteArticle } = useFavoriteArticle({
      isFavorited: computed(() => article.value.favorited),
      articleSlug: computed(() => article.value.slug),
      updateArticle: newArticle => emit('update', newArticle),
    })

    const onDelete = async () => {
      await deleteArticle(article.value.slug)
      return router.push({ name: 'global-feed' })
    }

    const { followProcessGoing, toggleFollow: toggleFollowAuthor } = useFollow({
      following: computed(() => article.value.author.following),
      username: computed(() => article.value.author.username),
    })

    async function toggleFollow () {
      const author = await toggleFollowAuthor()
      const newArticle = { ...article.value, author }
      emit('update', newArticle)
    }

    const displayFollow = computed(() => (
      user.value !== null &&
      user.value.username !== article.value.author.username
    ))

    return {
      displayFollow,
      displayEditButton,
      onDelete,
      favoriteProcessGoing,
      favoriteArticle,
      followProcessGoing,
      toggleFollow,
    }
  },
})
</script>

<style scoped>
.space {
  margin-right: 8px;
}
</style>
